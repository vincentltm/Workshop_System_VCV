    static inline float getInterpolatedVoltage(float angle, int buttonIndex, int outputIndex) {
        // Normalize Angle (-150 to 150) to Table Index (0.0 to 6.0)
        // Range is 300 degrees total. 6 segments of 50 degrees each.
        float pos = (angle + 150.0f) / 50.0f;

        // Clamp to safe bounds
        if (pos < 0.0f) pos = 0.0f;
        if (pos > 6.0f) pos = 6.0f;

        int idxLow = (int)std::floor(pos);
        int idxHigh = std::min(idxLow + 1, 6);
        float t = pos - (float)idxLow;

        float valLow = (float)TABLE[idxLow][outputIndex][buttonIndex];
        float valHigh = (float)TABLE[idxHigh][outputIndex][buttonIndex];

        // Linear interpolation
        float rawADC = valLow * (1.0f - t) + valHigh * t;

        // Convert ADC (0-65535) to normalized scale (-1.0 to 1.0)
        float normalized = (rawADC - 32768.0f) / 32768.0f;

        // Scale to ±6V range (as specified by user)
        return normalized * 6.0f;
    }
